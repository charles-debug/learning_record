# 按经验将不同的数据存为不同的类型
# 验证这些数据是什么类型

# int -- 整型

num1 = 1
print(type(num1))

# float --浮点型

num2 = 1.1

print(type(num2))

# str --字符串 特点：数据都要带引号
a='hello world'
print(type(a))

# bool --布尔型
b=True
print(type(b))

# list --列表
c= [10,20,30]
print(type(c))

# tuple -- 元组
d=(10,20,30)
print(type(d))

# set --集合
e={10,20,30}
print(type(e))

# dict --字典
f= {'name':'TOM', 'age':18}
print(type(f))